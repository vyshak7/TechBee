package admin;

public class Student {
private int id;
private String name;
private int roll_no;
private int age;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public int getRoll_no() {
	return roll_no;
}
public void setRoll_no(int roll_no) {
	this.roll_no = roll_no;
}
public int getAge() {
	return age;
}
public void setAge(int age) {
	this.age = age;
}


	
	
}
